#include <unistd.h>

void	ft_print_comb(void)
{
	char	a;
	char	b;
	char	d;

	a = '0' - 1;
	while (++a <= '9')
	{
		b = a;
		while (++b <= '9')
		{
			d = b + 1;
			while (d <= '9')
			{
				write(1, &a, 1);
				write(1, &b, 1);
				write(1, &d, 1);
				if (a != '7' || b != '8' || d != '9')
				{
					write(1, ", ", 2);
				}
				d++;
			}
		}
	}
}
