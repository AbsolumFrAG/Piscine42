void	ft_rev_int_tab(int *tab, int size)
{
	int		x;
	int		y;
	int		t;

	x = size - 1;
	y = 0;
	while (y < x)
	{
		t = tab[x];
		tab[x] = tab[y];
		tab[y] = t;
		y++;
		x--;
	}
}
